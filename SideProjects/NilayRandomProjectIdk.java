package SideProjects;
class NilayRandomProjectIdk
{
    public static void main()
    {
        for(int i = 5;i>=1;i--)
        {
            for(int j = 1;j<=i;j++)
            {
                System.out.print(j + " ");
            }
            System.out.println();
            for(int k = 0;k<=5-i;k++)
            {
                System.out.print(" ");
            }
        }
    }
}